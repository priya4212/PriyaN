
public class Ascii {

	public static void main(String[] args) {
		for(int i='A';i<'Z';i++)
		{
			System.out.println(i);
		}
		System.out.println();
		for(int i='a';i<'z';i++)
		{
			System.out.println(i);
		}
		System.out.println();
		for(int i=0;i<=9;i++)
		{
			System.out.println(i+48);
		}
		System.out.println();
		for(int i=0;i<15;i++)
		{
			System.out.println(i+33);
		}
		
	}

}
